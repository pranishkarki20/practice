#include <stdio.h>
 int main() {
     int n ; 
     do {
        printf("Enter a number ");
        scanf("%d", &n);
        if(n%7 == 0) {
          break;
        }
     } while (1);
     printf("you entered a multiple of 7: %d\n", n);
     return 0;
 }
